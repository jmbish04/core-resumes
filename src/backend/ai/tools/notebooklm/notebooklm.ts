/**
 * Construct an authenticated {@link NotebookLMClient} by bypassing the
 * SDK's `connect()` method and building the auth object ourselves.
 *
 * Flow:
 *   1. Read session cookies from KV
 *   2. Get or fetch CSRF token (cached with sliding-window TTL)
 *   3. Construct `NotebookLMClient` directly with pre-built auth
 *
 * This eliminates the SDK's bare `fetch()` to notebooklm.google.com which
 * was detected as non-browser access by Google.
 */
export async function createNotebookClient(env: Env): Promise<NotebookLMClient> {
  const fastapiUrl = (env as any).NOTEBOOKLM_FASTAPI_URL;
  const fastapiKey = (env as any).NOTEBOOKLM_FASTAPI_KEY;

  if (fastapiUrl) {
    console.info(`[createNotebookClient] Proxying NotebookLM calls to FastAPI server at ${fastapiUrl}`);
    return new NotebookLMFastAPIProxy(env, fastapiUrl, fastapiKey) as unknown as NotebookLMClient;
  }

  const cookieString = await getNotebookLMCookies(env);

  if (!cookieString || cookieString.length < 10) {
    throw new Error(
      "NotebookLM session cookies not found. " +
        "Update from Config → NotebookLM Session, or run: pnpm run session:sync",
    );
  }

  const auth = await getOrFetchAuth(env, cookieString);

  // Build the auth object that NotebookLMClient constructor expects
  // This mirrors what the SDK's connect() function returns
  const cookieMap: Record<string, string> = {};
  for (const part of cookieString.split(/;\s*/)) {
    const idx = part.indexOf("=");
    if (idx > 0) {
      const name = part.slice(0, idx).trim();
      const value = part.slice(idx + 1).trim();
      if (name) cookieMap[name] = value;
    }
  }

  const sdkAuth = {
    cookies: cookieMap,
    csrfToken: auth.csrfToken,
    sessionId: auth.sessionId,
    cookieHeader: auth.cookieHeader,
    googleCookieHeader: auth.cookieHeader,
  };

  // @ts-expect-error — NotebookLMClient constructor is private but we need to
  // bypass connect() which uses a bare fetch() that triggers Google bot detection.
  // Our auth object is built from Browser Rendering with a Chrome User-Agent.
  return new NotebookLMClient(sdkAuth, { timeoutMs: 30_000 });
}

